public class BehevioralCheckException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public BehevioralCheckException() {
		super();
	}

	public BehevioralCheckException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public BehevioralCheckException(String message, Throwable cause) {
		super(message, cause);
	}

	public BehevioralCheckException(String message) {
		super(message);
	}

	public BehevioralCheckException(Throwable cause) {
		super(cause);
	}

	
	
}
